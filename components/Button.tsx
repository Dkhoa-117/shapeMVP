import React from 'react';

interface ButtonProps extends React.ButtonHTMLAttributes<HTMLButtonElement> {
  variant?: 'primary' | 'secondary' | 'ghost' | 'outline';
  size?: 'sm' | 'md' | 'lg';
  children: React.ReactNode;
}

const Button: React.FC<ButtonProps> = ({ 
  variant = 'primary', 
  size = 'md', 
  className = '', 
  children, 
  ...props 
}) => {
  const baseStyles = "inline-flex items-center justify-center font-medium transition-colors focus:outline-none focus:ring-2 focus:ring-offset-2 disabled:opacity-50 disabled:cursor-not-allowed rounded-md";
  
  const variants = {
    // Primary: Orange minium
    primary: "bg-primary text-white hover:bg-[#c03f0a] focus:ring-primary",
    // Secondary: Bleu nuit
    secondary: "bg-secondary text-white hover:bg-[#0d1a29] focus:ring-secondary",
    outline: "border border-gray-300 text-dark bg-transparent hover:bg-gray-50 focus:ring-gray-500",
    ghost: "bg-transparent text-primary hover:bg-[#FFF4ED] focus:ring-[#E75113]",
  };

  const sizes = {
    sm: "px-3 py-1.5 text-sm",
    md: "px-5 py-2.5 text-base",
    lg: "px-6 py-3.5 text-lg",
  };

  return (
    <button 
      className={`${baseStyles} ${variants[variant]} ${sizes[size]} ${className}`}
      {...props}
    >
      {children}
    </button>
  );
};

export default Button;